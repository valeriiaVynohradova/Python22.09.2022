# Задача 1.Сформуйте строку, яка містить певну інформацію про символ по його індексу в відомому слові.
# Наприклад "The [індекс символу] symbol in [тут слово] is '[символ з відповідним порядковим номером]'".
# Слово та номер отримайте за допомогою input() або скористайтеся константою.
# Наприклад (слово - "Python" а номер символу 3) - "The 3 symbol in "Python" is 't' ".

word = input('Введіть слово:')
number = int(input('Введіть номер символа:'))
result = word[number - 1]
print('The', number, 'symbol in', '"' + word + '"', 'is', "'" + result + "'", end='.')

# Задача 2. Ввести з консолі строку зі слів за допомогою input() (або скористайтеся константою).
# Напишіть код, який визначить кількість слів, в цих даних.

